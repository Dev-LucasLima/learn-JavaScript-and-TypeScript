/*
for loop -> for (inicializable; condition; increment/decrement)
*/

for (let variable = 0; variable < 10; variable++) {
    console.log("current value of variable: ", variable);
}

/*
while loop -> while (condition)
*/
let counter = 0;
while (counter < 10) {
    console.log("current value of counter: ", counter);
    counter++;
}
